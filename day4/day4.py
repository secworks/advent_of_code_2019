# Solutions to Advent of Code 2019, day 4.



#-------------------------------------------------------------------
#-------------------------------------------------------------------
def get_input(filename):
    l = []
    with open(filename,'r') as f:
        for line in f:
            l.append(line.strip())
    return l


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def check_sequence2(n):
    if (((n[0] == n[1]) and (n[1] != n[2])) or ((n[0] != n[1])
        and (n[1] == n[2]) and (n[2] != n[3])) or ((n[1] != n[2]) and
            (n[2] == n[3]) and (n[3] != n[4])) or ((n[2] != n[3]) and
            (n[3] == n[4]) and (n[4] != n[5])) or ((n[3] != n[4]) and
            (n[4] == n[5]))):
        return True
    else:
        return False


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def check_sequence1(n):
    if ((n[0] == n[1]) or (n[1] == n[2]) or (n[2] == n[3]) or
        (n[3] == n[4]) or (n[4] == n[5])):
        return True
    else:
        return False


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def check_monotonic(n):
    if ((n[0] > n[1]) or (n[1] > n[2]) or (n[2] > n[3]) or
            (n[3] > n[4]) or (n[4] > n[5])):
        return False
    else:
        return True

#-------------------------------------------------------------------
#-------------------------------------------------------------------
def matches2(n):
    l = [int(x) for x in str(n)]
    return check_monotonic(l) and check_sequence2(l)


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def matches1(n):
    l = [int(x) for x in str(n)]
    return check_monotonic(l) and check_sequence1(l)


#-------------------------------------------------------------------
# Puzzle input: [278384..824795]
#-------------------------------------------------------------------
def problem1():
    print("Problem 1")
    matched = []
    for n in range(278384, 824795):
        if matches1(n):
            matched.append(n)
    print(len(matched))


